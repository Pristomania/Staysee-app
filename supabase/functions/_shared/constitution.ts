// ── Prompt assembly (для будущей инъекции) ───────────────────────────────────

export interface ConstitutionPromptOptions {
  /** Краткая ссылка на миссию (по умолчанию — полный блок). */
  includeMission?: boolean;
  /** Включить принципы конституции (по умолчанию true). */
  includeConstitution?: boolean;
  /** Включить принципы внимания (по умолчанию true). */
  includeAttention?: boolean;
}

/**
 * Собирает конституционный блок для system prompt.
 */
export function buildConstitutionPrompt(
  options: ConstitutionPromptOptions = {}
): string {
  const {
    includeMission = true,
    includeConstitution = true,
    includeAttention = true,
  } = options;

  const parts: string[] = [];

  if (includeMission) {
    parts.push(STAYSY_MISSION);
  }

  if (includeConstitution) {
    parts.push(
      "КОНСТИТУЦИЯ STAYSEE (внутреннее, не цитировать пользователю):\n" +
        STAYSY_CONSTITUTION_PRINCIPLES.map((p) => `• ${p}`).join("\n")
    );
  }

  if (includeAttention) {
    parts.push(
      "ПРИНЦИПЫ ВНИМАНИЯ (внутреннее):\n" +
        STAYSY_ATTENTION_PRINCIPLES.map((p) => `• ${p}`).join("\n")
    );
  }

  if (parts.length === 0) return "";
  return parts.join("\n\n");
}

